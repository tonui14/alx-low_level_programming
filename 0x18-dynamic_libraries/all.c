#include <stdio.h>
/**
 * add - add two integers
 * @a: first int
 * @b: second int
 * Return: addition
 */
 int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - sub two integers
 * @a: first int
 * @b: second int
 * Return: subtracted final result
 */
 int sub(int a, int b)
{
	return (a - b);
}
/**
 * div - divide two integers
 * @a: first int
 * @b: second int
 * Return: division
 */
 int div(int a, int b)
{
	return (a / b);
}
/**
 * mod - add two integers
 * @a: first int
 * @b: second int
 * Return: final answer
 */
 int mod(int a, int b)
{
	return (a % b);
}
/**
 * mul - mul two integers
 * @a: first int
 * @b: second int
 * Return: product
 */
 int mul(int a, int b)
{
	return (a * b);
}
